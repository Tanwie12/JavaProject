    private void WaitingQueue(){
        try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentsdb", "root", "root");
        conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/booksdb", "root", "root");
        stmt = conn.createStatement();
         
        DefaultTableModel table = (DefaultTableModel)wqptable.getModel();
        
        String PRINT = "SELECT* FROM students JOIN books;";
        
        rs = stmt.executeQuery(PRINT);
        while (rs.next()) {
            String student_ID = rs.getString("student_id");
            String student_Name = rs.getString("student_name");
            String book_ID = rs.getString("book_id");
            String book_title = rs.getString("book_title");
            String date_booked = rs.getString("date_booked");
            table.addRow(new Object[] { student_ID, student_Name, book_ID, book_title, date_booked });
        }
        rs.close();
        stmt.close();
        conn.close();
    }
    catch (Exception e) {
        System.out.println(e);
        JOptionPane.showMessageDialog(this, "Error In Connectivity");
    }
    }
    
